/*
data 	指向数据的指针
buf		指向缓存区的指针
len		数据长度
dir		方向	0从头到尾 1从尾到头
返回值	传输的字节数量
*/
uint16_t VoidDataToBuf(void *data,void *buf,uint16_t len,uint8_t dir)
{
	uint8_t *p_data = (uint8_t *)data;
	uint8_t *p_buf  = (uint8_t *)buf;
	uint16_t index = dir?(len-1):0;
	for(uint8_t i=0;i<len;i++)
	{
		p_buf[i] = p_data[index];
		if(dir)	{index--;}
		else		{index++;}
	}
	return len;
}

void BigLitterTurn(void *data,uint8_t len)
{
  uint8_t *p = (uint8_t *)data,data_buf = 0,cnt = len/2;
  
  if(p==NULL || (len%2)!=0){return;}
  
  for(uint8_t i=0;i<cnt;i++)
  {
    data_buf=p[i];
    p[i]=p[len-i-1];
    p[len-i-1] = data_buf;
  }
}

