void hex2str(uint8_t *str, uint8_t *hex, uint32_t len)
{
    uint32_t i;
    uint8_t l4,h4;
    for(i = 0; i < len; i++) 
    {
        h4 = (hex[i] & 0xf0) >> 4;
        l4 = hex[i] & 0x0f;
        if(h4 <= 9){
            str[2*i] = h4 + ('0' - 0);
        }
        else{
            str[2*i] = h4 + ('A' - 10);
        }
        if (l4 <= 9){
            str[2*i+1] = l4 + ('0' - 0);
        }
        else{
            str[2*i+1] = l4 + ('A'- 10);
        }
    }
    str[2*i] = 0;//字符串结尾 声明数组时注意多加1字节
}

